              <h2>PWA</h2>

              <p>Os Progressive Web App (PWA), consiste em um conjunto de técnicas a serem utilizadas no site de forma a fazê-lo ficar mais parecido com um aplicativo de celular. Dentre as técnicas e recursos utilizados:<br></p>
              <ul style="float: left">
                <li>Tamanho Responsivo</li>
                <p>A tela do site/app se adapta ao tamanho do aparelho utilizado.</p>

                <li>Acesso Offline</li>

                <p>Necessário apenas na primeira visita, após o site fica salvo na memória cache do navegador do celular, então as próximas conexões são quase instantâneas já que o site já está salvo no celular.</p>
                <li>Notificações</li>

                <p>Permitem envio de notificações para o usuário assim como é feito em aplicativos como o Facebook e Tinder.</p>
                <li>Instalável</li>
                <p>Pode-se salvar o site na tela inicial do smartphone com um ícone, assim as próximas conexões serão feitas através dele, e ao abrí-lo ele pode abrir em "Tela cheia" sem a barra de navegação do navegador, fazendo ficar mais parecido com um App.</p>
